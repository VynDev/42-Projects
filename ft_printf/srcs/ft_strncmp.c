#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*(unsigned char *)s1 == *(unsigned char *)s2 && n > 0)
	{
		if (*s1 == '\0')
			return (0);
		n--;
		s1++;
		s2++;
	}
	if (n == 0)
		return (0);
	return (*(unsigned char *)s1 - *(unsigned char *)s2);
}
