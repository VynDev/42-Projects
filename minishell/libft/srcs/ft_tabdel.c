#include "libft.h"

void	ft_tabdel(char **tab)
{
	int		i;

	i = 0;
	if (!tab)
		return ;
	while (*(tab + i) != 0)
	{
		free(*(tab + i));
		++i;
	}
	free(*(tab + i));
	free(tab);
}
