#include <string.h>
#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t		i;

	i = ft_strlen(s);
	while (s + i >= s)
	{
		if (*(s + i) == c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
